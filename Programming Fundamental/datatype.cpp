#include <iostream>
using namespace std;
int main(){ 
    int a=10; // integer datatype
    float b=10.0f; // float datatype
    string c="Hello World"; // string datatype
    char d = 'A'; // character datatype
    long e = 10l; // l stand for long  (long datatype)
    double f = 120.897; // double datatype
    unsigned g = 1234; // unsigned int datatype
    signed h = -2343; // signed int datatype
    int i = 12.09l; // long double datatype 
    
    // Print all variable using cout function
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    cout << "d = " << d << endl;
    cout << "e = " << e << endl;
    cout << "f = " << f << endl;
    cout << "g = " << g << endl;
    cout << "h = " << h << endl;
    cout << "i = " << i << endl;
    
}